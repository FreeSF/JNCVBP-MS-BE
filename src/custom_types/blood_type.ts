/**
 * An enumeration of the different blood types.
 */
export enum BloodType {
  NotSet = "Not Set", //The user's blood type is not recorded.
  APositive = "A+", // The A+ blood type.
  ANegative = "A-", // The A- blood type.
  BPositive = "B+", //The B+ blood type.
  BNegative = "B-", // The B- blood type.
  ABPositive = "AB+", // The AB+ blood type
  ABNegative = "AB-", //The AB- blood type.
  OPositive = "0+", //The O+ blood type.
  ONegative = "0-", //The O- blood type.
}
